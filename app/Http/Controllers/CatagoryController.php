    {
        $this->validate($request,array(
            
            'name'  =>'required |max:199',

        ));
        $catagory = new Catagory;

        $catagory->name = $request->input('name');
        $catagory->save();
        
        session()->flash('success', 'catagory Successfully Created!');

        return redirect()->route('catagories.index');
    }

